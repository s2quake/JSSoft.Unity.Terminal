using System;
using UnityEngine;

namespace JSSoft.Unity.Terminal.InputHandlers
{
    class SwipedEventArgs : EventArgs
    {
        public SwipedEventArgs(SwipeDirection direction, Vector2 startPos, Vector2 endPos)
        {
            this.Direction = direction;
            this.StartPos = startPos;
            this.EndPos = endPos;
        }

        public SwipeDirection Direction { get; }

        public Vector2 StartPos { get; }

        public Vector2 EndPos { get; }
    }
}

using System;
using System.Collections.Generic;

namespace JSSoft.Unity.Terminal
{
    public class TextChangedEventArgs : EventArgs
    {
        public TextChangedEventArgs(params TextChange[] changes)
        {
            var itemList = new List<TextChange>(changes.Length);
            foreach (var item in changes)
            {
                if (item.Length != 0)
                    itemList.Add(item);
            }
            this.Changes = itemList.ToArray();
        }

        public TextChange[] Changes { get; }
    }
}
